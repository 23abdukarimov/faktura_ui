function initPricing() {
  const billingToggle = document.getElementById('billingToggle');
  const monthlyLabel = document.getElementById('billingMonthlyLabel');
  const yearlyLabel = document.getElementById('billingYearlyLabel');

  if (billingToggle) {
    billingToggle.addEventListener('change', () => {
      if (billingToggle.checked) {
        billingPeriod = 'yearly';
        if (yearlyLabel) yearlyLabel.classList.add('active');
        if (monthlyLabel) monthlyLabel.classList.remove('active');
      } else {
        billingPeriod = 'monthly';
        if (monthlyLabel) monthlyLabel.classList.add('active');
        if (yearlyLabel) yearlyLabel.classList.remove('active');
      }
      updatePrices();
    });
  }

  // Tabs on price.html
  const tabNew = document.getElementById('tabNewTariffs');
  const tabArchive = document.getElementById('tabArchiveTariffs');
  const tabPackages = document.getElementById('tabDocPackages');

  const gridNew = document.getElementById('gridNewTariffs');
  const gridArchive = document.getElementById('gridArchiveTariffs');
  const gridPackages = document.getElementById('gridDocPackages');

  if (tabNew && tabArchive && tabPackages) {
    // Hide others initially on page load
    if (gridArchive) gridArchive.style.display = 'none';
    if (gridPackages) gridPackages.style.display = 'none';
    if (gridNew) {
      gridNew.style.display = 'block';
      gridNew.classList.add('show');
    }

    const activateTab = (activeTab, activeGrid) => {
      [tabNew, tabArchive, tabPackages].forEach(t => t.classList.remove('active'));
      [gridNew, gridArchive, gridPackages].forEach(g => {
        if (g) {
          g.classList.remove('show');
          g.style.display = 'none';
        }
      });

      activeTab.classList.add('active');
      if (activeGrid) {
        activeGrid.style.display = 'block';
        activeGrid.offsetHeight; // Force reflow
        activeGrid.classList.add('show');
      }
    };

    tabNew.addEventListener('click', () => activateTab(tabNew, gridNew));
    tabArchive.addEventListener('click', () => activateTab(tabArchive, gridArchive));
    tabPackages.addEventListener('click', () => activateTab(tabPackages, gridPackages));
  }
}


